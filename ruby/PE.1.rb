def sum_multiples(x):
    lst = []
    total = 0 
    for i in range(1,x):
        if i % 3 == 0:
            lst.append(i)
        elif i % 5 == 0:
            lst.append(i)
        else:
            continue
    for y in lst:
        total += y
    print total
    

sum_multiples(1000)

sum_multiples(10)


